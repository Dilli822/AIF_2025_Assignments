@lru_cache(maxsize=1)
def load_model_and_assets():
    """Loads the trained model and label encoder."""
    if not MODEL_PATH.exists():
        logger.error(f"Model file not found at {MODEL_PATH}")
        raise FileNotFoundError(f"Model file not found at {MODEL_PATH}")

    try:
        model = joblib.load(MODEL_PATH)
        le = joblib.load(ENCODER_PATH)
        logger.info("Model and assets loaded successfully")
        return model, le, symptom_columns
    except Exception as e:
        logger.error(f"Error loading model or assets: {e}", exc_info=True)
        raise


def preprocess_user_input(symptom_input: schemas.Symptoms, symptom_columns):
    input_symptoms = [
        symptom for symptom, present in symptom_input.model_dump().items() if present
    ]

    if not input_symptoms:
        logger.warning("No symptoms provided by user.")
        return None

    input_vector = [
        1 if symptom in input_symptoms else 0 for symptom in symptom_columns
    ]
    return np.array(input_vector).reshape(1, -1)


def predict(symptom_input: schemas.Symptoms):
    # Load model and required assets
    model, le, symptom_columns = load_model_and_assets()

    # Preprocess
    input_vector = preprocess_user_input(symptom_input, symptom_columns)

    if input_vector is None:
        return "Please Enter Symptoms! Symptoms not found"

    # Predict
    prediction = model.predict(input_vector)
    predicted_label = le.inverse_transform(prediction)[0]

    return predicted_label
